# include <vector>
# include <iostream>

void print(const char character) {
    std::cout << character;
}

void print(const char *string) {
    std::cout << string;
}

void print(const int number) {
    std::cout << number;
}

void println(const char character) {
    std::cout << character << '\n';
}

void println(const char *string) {
    std::cout << string << '\n';
}

void println(const int number) {
    std::cout << number << '\n';
}

